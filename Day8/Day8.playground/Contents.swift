enum errors :Error {
    case outOfBound, noRoot
}
//íŒë³„
func sqrtNum(_ num:Int) throws -> Int {
    //1ë³´ë‹¤ ì‘ê±°ë‚˜ 10,000ë³´ë‹¤ í¬ë©´
    if num<1 || num>10_000 {
        throw errors.outOfBound
    }
    for i in 1...(num/2+1){
        if i*i == num {
            return i
        }
    }
    throw errors.noRoot
}
func input(){
    let num = 10000//Int(readLine()!)!
    
    do{
        let n = try sqrtNum(num)
            print("\(n) is a SqrtNum of \(num)")
    }catch errors.outOfBound{
        print ("Out of bound")
    }catch errors.noRoot {
        print("No root")
    }
    catch {
        print("error")
    }
    
}
